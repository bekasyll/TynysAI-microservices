    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    patient_id UUID NOT NULL,
    added_by_doctor_id UUID,
    test_type VARCHAR(40) NOT NULL,
    test_date DATE NOT NULL,
    lab_name VARCHAR(150),
    hemoglobin DOUBLE PRECISION,
    wbc DOUBLE PRECISION,
    rbc DOUBLE PRECISION,
    platelets DOUBLE PRECISION,
    hematocrit DOUBLE PRECISION,
    neutrophils DOUBLE PRECISION,
    lymphocytes DOUBLE PRECISION,
    monocytes DOUBLE PRECISION,
    eosinophils DOUBLE PRECISION,
    crp DOUBLE PRECISION,
    esr DOUBLE PRECISION,
    pro_calcitonin DOUBLE PRECISION,
    ferritin DOUBLE PRECISION,
    ldh DOUBLE PRECISION,
    d_dimer DOUBLE PRECISION,
    glucose DOUBLE PRECISION,
    creatinine DOUBLE PRECISION,
    urea DOUBLE PRECISION,
    albumin DOUBLE PRECISION,
    total_protein DOUBLE PRECISION,
    alt DOUBLE PRECISION,
    ast DOUBLE PRECISION,
    bilirubin DOUBLE PRECISION,
    ph DOUBLE PRECISION,
    pao2 DOUBLE PRECISION,
    paco2 DOUBLE PRECISION,
    hco3 DOUBLE PRECISION,
    spo2 DOUBLE PRECISION,
    fev1 DOUBLE PRECISION,
    fvc DOUBLE PRECISION,
    fev1_fvc_ratio DOUBLE PRECISION,
    culture_result VARCHAR(500),
    pathogen_found VARCHAR(200),
    sensitivity_result VARCHAR(500),
    igra_result VARCHAR(10),
    mantoux_result VARCHAR(10),
    mantoux_indurat_mm INTEGER,
    pcr_result VARCHAR(20),
    pcr_ct_value DOUBLE PRECISION,
    notes TEXT,
    raw_result_text TEXT,
    attachment_path VARCHAR(500),
    created_at TIMESTAMP
