CREATE TABLE IF NOT EXISTS diagnostic_reports (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    patient_id UUID NOT NULL,
    doctor_id UUID NOT NULL,
    xray_analysis_id BIGINT,
    lab_result_id BIGINT,
    final_diagnosis VARCHAR(30) NOT NULL,
    severity VARCHAR(15) NOT NULL,
    clinical_findings TEXT NOT NULL,
    treatment_recommendations TEXT,
    medication_recommendations TEXT,
    lifestyle_recommendations TEXT,
    follow_up_date DATE,
    report_text TEXT NOT NULL,
    attachment_path VARCHAR(500),
    sent_to_patient BOOLEAN NOT NULL,
    sent_at TIMESTAMP,
    report_number VARCHAR(255),
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_report_patient ON diagnostic_reports (patient_id);
CREATE INDEX IF NOT EXISTS idx_report_doctor ON diagnostic_reports (doctor_id);

